package com.example.bublovskiy.moveaview;

//to store line coordinates
public class Line
{
    //the line's coordinates
    public float x1,x2,y1,y2;
    int number;

   public Line(float x1,float y1,float x2,float y2, int number) {
       this.x1 = x1;
       this.y1 = y1;
       this.x2 = x2;
       this.y2 = y2;
       this.number = number;

   }//end Line constructor




}//end Line class
